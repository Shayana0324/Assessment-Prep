// Finding the square and cube of a number
"use strict";

const a = 3;

// Square of a number
function numSquare(a) {
    let sqr = a ** 2;
    console.log("Sqaure = " + sqr);
}

numSquare(a);

// Cube of a number
function numCube(a) {
    let cube = a ** 3;
    console.log("Cube = " + cube);
}

numCube(a);